;*****************************************************************
;* Func:	LCD_Internal_WriteLn
;* Desc:	Sends 16 characters of information to the LCD panel
;*		This is slightly more complicated than it sounds because the
;*		characters straddle two banks of the LCD memory.
;*		X is pointing at the first ASCII character to be displayed.
;*		r17 holds the index of the destination bank in LCD RAM.
;*		
;*****************************************************************
LCD_Internal_WriteLn:
	push	r0
	push	r1
	push	mpr
	push	r18
	push	r19
	push	r24
	push	r25
	push	ZL
	push	ZH

	; r25:24 holds a pointer to the base address of the bitmap array
	ldi	r25, HIGH(FontBitmap << 1);
	ldi	r24, LOW(FontBitmap << 1);

	cbi	PORTB, 0	; activate slave select

	; use a counter to track if we are on the top chunk or bottom chunk of each char
	ldi	r18, 2

LCD_WL_display_bank:
	; set the column to 0
	ldi	mpr, lcd_c_disp_set_col_addr_h
	rcall	LCD_Internal_WriteCMD
	ldi	mpr, lcd_c_disp_set_col_addr_l
	rcall	LCD_Internal_WriteCMD
	; we need to select the appropriate page
	ldi	mpr, lcd_c_disp_set_page_addr
	or	mpr, r17	; set the lower 4 bits
	rcall	LCD_Internal_WriteCMD

	ldi	r19, 16	; there are 16 characters per line

LCD_WL_next_char:
	; get ascii code point & determine location of corresponding character bitmap
	ld	r0, X+	; this should be a value from 0 to 255
	ldi	mpr, 16	; there are 16 bytes per character
	mul	r0, mpr	; determine the byte offset for the desired char
	movw	ZH:ZL, r25:r24	; copy the base address to Z
	add	ZL, r0	; finally, compute a pointer to the char bitmap
	adc	ZH, r1

	ldi	mpr, 8	; there are 8 columns for each character

	; this is where we display the chunk of character (either a top chunk or bottom chunk)
	lpm	r0, Z
LCD_WL_next_col:
	out	SPDR, r0
	sbi	PORTF, 1	; set lcd_A0 to specify data mode
	adiw	ZH:ZL, 2	; point Z at the next column for this bank
	lpm	r0, Z		; this is a sneaky way to save a few clock cycles (since we are waiting for the SPI communication)
LCD_WL_wait_spi:
	in	r1, SPSR
	sbrs	r1, SPIF	; wait for SPI to finish
	rjmp	LCD_WL_wait_spi

	; keep going until we have displayed all the columns for this character
	dec	mpr
	brne	LCD_WL_next_col

	; move to the next character
	dec	r19
	brne	LCD_WL_next_char

	; if we are at the end of the line, configure the bank and starting array offset for the bottom half of each char
	dec	r18		; did we display both banks yet?
	breq	LCD_WL_all_done
	; if not, then prepare to display the bottom chunk of each character
	dec	r17		; decrement the bank number
	sbiw	XH:XL, 16	; X needs to point back at the beginning character of the line
	adiw	r25:r24, 1	; increment the base address pointer (now it will point to the second chunk of each char)
	rjmp	LCD_WL_display_bank

LCD_WL_all_done:
	sbi	PORTB, 0	; deactivate slave select

	pop	ZH
	pop	ZL
	pop	r25
	pop	r24
	pop	r19
	pop	r18
	pop	mpr
	pop	r1
	pop	r0
	ret

;*****************************************************************
;* Func:	LCD_Internal_ClearLn
;* Desc:	This function clears two of the LCD memory banks.
;*		r17 specifies the starting bank to wipe.
;*****************************************************************
LCD_Internal_ClearLn:
	push	mpr
	push	r1
	push	r18
	push	r19

	cbi	PORTB, 0	; activate slave select
	ldi	r19, 2	; we are going to clear two banks

LCD_CLRLN_clear_bank:
	; set the column to 0
	ldi	mpr, lcd_c_disp_set_col_addr_h
	rcall	LCD_Internal_WriteCMD
	ldi	mpr, lcd_c_disp_set_col_addr_l
	rcall	LCD_Internal_WriteCMD
	; we need to select the appropriate page
	ldi	mpr, lcd_c_disp_set_page_addr
	or	mpr, r17	; configure the lower 4 bits
	rcall	LCD_Internal_WriteCMD

	ldi	r18, 128	; there are 128 columns on the LCD
	clr	mpr		; we will fill each column with empty dots (i.e. blank space)
	sbi	PORTF, 1	; set lcd_A0 to specify data mode

LCD_CLRLN_write_col:
	out	SPDR, mpr	; send an empty column to the LCD via SPI
LCD_CLRLN_wait_spi:
	in	r1, SPSR
	sbrs	r1, SPIF	; wait for SPI to finish
	rjmp	LCD_CLRLN_wait_spi

	dec	r18
	brne	LCD_CLRLN_write_col	; cycle through all 128 columns

	; clear the remaining bank if we haven't done so already
	dec	r19
	breq	LCD_CLRLN_all_done
	dec	r17		; decrement the bank index
	rjmp	LCD_CLRLN_clear_bank

LCD_CLRLN_all_done:
	sbi	PORTB, 0	; deactivate slave select	
	
	; pop registers...
	pop	r19
	pop	r18
	pop	r1
	pop	mpr
	ret

;*****************************************************************
;* Func:	LCD_Internal_WriteCMD
;* Desc:	Sends the byte in mpr
;*		If you use this function directly,
;*		YOU ARE RESPONSIBLE for handling the slave select line!
;*****************************************************************
LCD_Internal_WriteCMD:
	out	SPDR, mpr	; initiate transmission
	cbi	PORTF, 1	; clear lcd_A0 to put the lcd into command mode
				; (this just has to be done before the 8th bit
				; is transmitted via SPI)
LCD_IWC_L1:
	in	mpr, SPSR
	sbrs	mpr, SPIF	; wait for SPI to finish
	rjmp LCD_IWC_L1
	ret

;*****************************************************************
;* Func:	LCDDelay
;* Desc:	A wait loop that will consume roughly 1ms
;*		Note that a CALL takes 4 cycles on top of this delay.
;*****************************************************************
LCDDelay:
	push	mpr		; 2 cycles
	push	r17		; 2 cycles
	ldi	r17, 11	; 1 cycle
LCDDelay_L1:
	ldi	mpr, 242	; 1 cycle
LCDDelay_L2:
	dec	mpr		; 1 cycle
	brne	LCDDelay_L2	; 2 cycles on branch, 1 if no branch
	dec	r17		; 1 cycle
	brne	LCDDelay_L1	; 2 cycles on branch, 1 if no branch
	pop	r17		; 2 cycles
	pop	mpr		; 2 cycles
	ret			; 4 cycles

;*****************************************************************
;* Func:	div10
;* Desc:	Divides the value in the mpr by 10 and 
;*			puts the remainder in the 'r' register
;*			and the quotient in the 'q' register.
;*	DO NOT modify this function, trust me, it does
;*	divide by 10 :)  ~DZ		
;*****************************************************************
div10:
	push	r0		; Save register

	; q = mpr / 10 = mpr * 0.000110011001101b
	mov	q, mpr	; q = mpr * 1.0b
	lsr	q		; q >> 2
	lsr	q		; q = mpr * 0.01b
	add	q, mpr	; q = (q + mpr) >> 1
	lsr	q		; q = mpr * 0.101b
	add	q, mpr	; q = (q + mpr) >> 3
	lsr	q
	lsr	q
	lsr	q		; q = mpr * 0.001101b
	add	q, mpr	; q = (q + mpr) >> 1
	lsr	q		; q = mpr * 0.1001101b
	add	q, mpr	; q = (q + mpr) >> 3
	lsr	q				
	lsr	q
	lsr	q		; q = mpr * 0.0011001101b
	add	q, mpr	; q = (q + mpr) >> 1
	lsr	q		; q = mpr * 0.10011001101b
	add	q, mpr	; q = (q + mpr) >> 4
	lsr	q
	lsr	q
	lsr	q
	lsr	q		; q = mpr * 0.000110011001101b

	; compute the remainder as r = i - 10 * q
	; calculate r = q * 10 = q * 1010b
	mov	r, q		; r = q * 1
	lsl	r		; r << 2
	lsl	r		; r = q * 100b
	add	r, q		; r = (r + q) << 1
	lsl	r		; r = q * 1010b
	mov	r0, r		; r0 = 10 * q
	mov	r, mpr	; r = mpr
	sub	r, r0		; r = mpr - 10 * q

	; Fix any errors that occur
div10_1:
	cpi	r, 10		; Compare with 10
	brlo	div10_2	; do nothing if r < 10
	inc	q		; fix quotient
	subi	r, 10		; fix remainder
	rjmp	div10_1	; Continue until error is corrected

div10_2:
	pop	r0		; Restore registers
	ret			; Return from function

